const EASY_DIFFICULTY_VALUE = "easy";
const MEDIUM_DIFFICULTY_VALUE = "medium";
const HARD_DIFFICULTY_VALUE = "hard";
const TIMER_MODE_VALUE = "timed";
const PASSAGE_MODE_VALUE = "passage";
const EASY_RADIO_NODE = 0;
const MEDIUM_RADIO_NODE = 1;
const HARD_RADIO_NODE = 2;
const TIMER_MODE_NODE = 0;
const PASSAGE_MODE_NODE = 1;
const difficultyValuesToNode = {
  [EASY_DIFFICULTY_VALUE]: EASY_RADIO_NODE,
  [MEDIUM_DIFFICULTY_VALUE]: MEDIUM_RADIO_NODE,
  [HARD_DIFFICULTY_VALUE]: HARD_RADIO_NODE,
};
const difficultyValuesToText = {
  [EASY_DIFFICULTY_VALUE]: "Easy",
  [MEDIUM_DIFFICULTY_VALUE]: "Medium",
  [HARD_DIFFICULTY_VALUE]: "Hard",
};
const modeValuesToNode = {
  [TIMER_MODE_VALUE]: TIMER_MODE_NODE,
  [PASSAGE_MODE_VALUE]: PASSAGE_MODE_NODE,
};
const modeValuesToText = {
  [TIMER_MODE_VALUE]: "Timed(60s)",
  [PASSAGE_MODE_VALUE]: "Passage",
};
const BACKSPACE_KEY = "Backspace";
const state = {
  loading: false,
  loaded: false,
  running: false,
};
const allowedKeyCodes = [
  "Key",
  "Period",
  "Comma",
  "Space",
  "Semicolon",
  "Quote",
  "Minus",
  "Digit",
  "Slash",
];
const wordsEl = document.getElementsByClassName("words")[0];
const wordsAppEl = document.getElementsByClassName("words-app")[0];
const difficultyEl = document.getElementsByClassName("difficulties")[0];
const modeEl = document.getElementsByClassName("modes")[0];
const currentDifficultyText = difficultyEl.getElementsByClassName(
  "current-difficulties",
)[0];
const difficultiesDropdown =
  difficultyEl.getElementsByClassName("dropdown-mobile")[0];
const modesDropdown = modeEl.getElementsByClassName("dropdown-mobile")[0];
const currentModeText = modeEl.getElementsByClassName("current-mode")[0];
const resultsEl = document.getElementsByClassName("results")[0];
const startBtn = document.getElementsByClassName("btn-start")[0];
const containerWords = document.getElementsByClassName("container")[0];
const startContainer = containerWords.getElementsByClassName("center-wrap")[0];
const wpmEl = document.getElementsByClassName("wpm")[0];
const accuracyEl = document.getElementsByClassName("accuracy")[0];
const timeViewEl = document.getElementsByClassName("time")[0];
const restartBtnContainer =
  document.getElementsByClassName("restart-container")[0];
const restartBtn = restartBtnContainer.getElementsByClassName("btn-restart")[0];
const restartFinishBtn =
  document.getElementsByClassName("btn-finish-restart")[0];
const finishBtnTitle = restartFinishBtn.querySelector("span");
const blurMask = document.getElementsByClassName("blur-mask")[0];
const bestResultEl = document.getElementsByClassName("best-result")[0];
const resultTitle = document.getElementsByClassName("test-finished-title")[0];
const resultDescription = document.getElementsByClassName(
  "test-finished-description",
)[0];
const mobileInput = document.getElementsByClassName("typing-input-mobile")[0];
const pieces = document.querySelectorAll(".confetti path");
const difficultyOptions = document.querySelectorAll(".difficulty");
const modeOptions = document.querySelectorAll(".mode");
const scoreWpmEl = document.querySelector(".scores-list .wpm");
const scoreAccuracyEl = document.querySelector(".scores-list .accuracy");
const correctCharsEl = document.querySelector(".scores-list .correct-chars");
const errorsCharsEl = document.querySelector(".scores-list .errors-chars");
const mobileTitleDropdown = document.querySelectorAll(".current-wrap");
const cursorChars = {
  typedChars: [],
  remainingChars: [],
};
let currentDifficulty = EASY_DIFFICULTY_VALUE;
let currentMode = TIMER_MODE_VALUE;
let errorChars = 0;
let correctChars = 0;
let wpm = 0;
let accuracy = 0;
let typedCharsCount = 0;
let timerId = null;

const formatTimer = (time) => {
  return time > 9 ? time : `0${time}`;
};

const resetText = () => {
  const spanNodes = wordsEl.querySelectorAll("span");
  for (const node of spanNodes) {
    node.className = "";
    if (node.classList.contains("current")) break;
  }
  spanNodes[0].classList.add("current");
  cursorChars.remainingChars = [
    ...cursorChars.typedChars,
    ...cursorChars.remainingChars,
  ];
};

const setTextData = (value) => {
  const { text } =
    textData[value][Math.floor(Math.random() * textData[value].length)];

  // Replace all em dashes "—" in the JSON text with a normal hyphen "-"
  // This is needed because the source text (from JSON) may contain em dashes,
  // which could break parsing or character comparisons in the typing logic
  return text.replace(/—/g, "-").split("");
};

const wrapCharsInSpans = () => {
  const html = cursorChars.remainingChars.map((char, index) => {
    if (index === 0) {
      return `<span class="current">${char}</span>`;
    }

    return `<span>${char}</span>`;
  });

  return html.join("");
};

const setupTest = () => {
  state.loading = true;
  resetStats();
  difficultyOptions[difficultyValuesToNode[currentDifficulty]].checked = true;
  modeOptions[modeValuesToNode[currentMode]].checked = true;
  cursorChars.remainingChars = setTextData(currentDifficulty);
  wordsEl.innerHTML = wrapCharsInSpans();
  blurMask.classList.add("active");
  restartBtnContainer.classList.add("hide");
  if (localStorage.getItem("bestWPM")) {
    bestResultEl.textContent = localStorage.getItem("bestWPM");
  }
  state.loading = false;
  state.loaded = true;
};

const resetStats = (onRestart) => {
  wpm = 0;
  accuracy = 0;
  typedCharsCount = 0;
  errorChars = 0;
  correctChars = 0;
  wpmEl.textContent = "0";
  accuracyEl.textContent = "100%";
  accuracyEl.className = "accuracy";
  timeViewEl.textContent = currentMode === TIMER_MODE_VALUE ? "00:60" : "00:00";
  timeViewEl.className = "time";
  if (!onRestart) {
    startContainer.classList.remove("hide");
    blurMask.classList.add("active");
  }
  if (cursorChars.typedChars.length > 0) {
    cursorChars.typedChars = [];
  }
};

const stopTest = () => {
  clearInterval(timerId);
  timerId = null;
  document.removeEventListener("keydown", handleKeydown);
  restartBtnContainer.classList.add("hide");
  state.running = false;
};

const setDifficulty = (evt) => {
  state.loading = true;
  if (evt) {
    const { value } = evt.target;
    if (currentDifficulty !== value) {
      currentDifficulty = value;
      currentDifficultyText.textContent = difficultyValuesToText[value];
      if (difficultiesDropdown.classList.contains("show")) {
        difficultiesDropdown.classList.remove("show");
      }
      if (state.running) {
        stopTest();
        resetStats();
      }
      cursorChars.remainingChars = setTextData(value);
      wordsEl.innerHTML = wrapCharsInSpans();
    }
  }
  state.loading = false;
  state.loaded = true;
};

const setMode = (evt) => {
  state.loading = true;
  if (evt) {
    const { value } = evt.target;
    if (currentMode !== value) {
      currentMode = value;
      currentModeText.textContent = modeValuesToText[value];
      if (modesDropdown.classList.contains("show")) {
        modesDropdown.classList.remove("show");
      }
      if (modeEl)
        if (state.running) {
          stopTest();
          resetText();
        }
      resetStats();
    }
  }
  state.loading = false;
  state.loaded = true;
};

const finishTest = () => {
  stopTest();
  wordsAppEl.classList.add("hide");
  resultsEl.classList.add("show");
  resultsEl.classList.remove("visually-hidden");
  scoreWpmEl.textContent = wpm;
  scoreAccuracyEl.textContent = accuracy + "%";
  accuracy === 100
    ? scoreAccuracyEl.classList.add("absolute")
    : scoreAccuracyEl.classList.add("below");
  correctCharsEl.textContent = correctChars;
  errorsCharsEl.textContent = errorChars;
  if (!localStorage.getItem("bestWPM")) {
    resultTitle.textContent = "Baseline Established!";
    resultDescription.textContent =
      "You've set the bar. Now the real challenge begins-time to beat it.";
    finishBtnTitle.textContent = "Beat This Score";
    localStorage.setItem("bestWPM", wpm);
  } else {
    if (wpm > localStorage.getItem("bestWPM")) {
      resultsEl.classList.add("new-score");
      resultTitle.textContent = "High Score Smashed!";
      resultDescription.textContent =
        "You're getting faster. That was incredible typing.";
      finishBtnTitle.textContent = "Beat This Score";
      pieces.forEach((piece, index) => {
        piece.style.transitionDelay = `${index * 0.01}s`;
      });
      localStorage.setItem("bestWPM", wpm);
    } else {
      resultTitle.textContent = "Test complete";
      resultDescription.textContent =
        "Solid run. Keep pushing to beat your high score.";
      finishBtnTitle.textContent = "Go Again";
    }
  }
};

const restartTestAfterFinish = (evt) => {
  evt.preventDefault();
  resultsEl.classList.remove("show");
  resultsEl.classList.remove("new-score");
  resultsEl.classList.add("visually-hidden");
  wordsAppEl.classList.remove("hide");
  startContainer.classList.remove("hide");
  setupTest();
};

const restartTest = (evt) => {
  evt.preventDefault();
  stopTest();
  resetText();
  resetStats(true);
  startTypingTest();
  mobileInput.focus({ preventScroll: true });
};

const handleKeydown = (evt) => {
  const isMobile = !evt.code;
  let allowKeyCodeValidation = allowedKeyCodes.some((allowedKey) =>
    evt.code.includes(allowedKey),
  );
  if (isMobile) {
    allowKeyCodeValidation = true;
  }
  if (allowKeyCodeValidation && cursorChars.remainingChars.length > 0) {
    const char = cursorChars.remainingChars.shift();
    const currentCharEl = document.getElementsByClassName("current")[0];
    cursorChars.typedChars.push(char);

    if (char === evt.key) {
      currentCharEl.classList.replace("current", "correct");
      correctChars++;
    } else {
      currentCharEl.classList.replace("current", "incorrect");
      errorChars++;
    }

    if (currentCharEl.nextElementSibling) {
      currentCharEl.nextElementSibling.classList.add("current");
    }

    typedCharsCount++;

    accuracy = Math.floor(
      ((typedCharsCount - errorChars) / typedCharsCount) * 100,
    );
    accuracyEl.textContent = accuracy + "%";
    accuracyEl.classList.toggle("below", accuracy < 100);
    accuracyEl.classList.toggle("absolute", accuracy === 100);
  }

  if (evt.code === BACKSPACE_KEY && cursorChars.typedChars.length !== 0) {
    const currentCharEl = document.getElementsByClassName("current")[0];
    const prevCharEl = currentCharEl.previousElementSibling;

    currentCharEl.classList.remove("current");

    prevCharEl.className = "";
    prevCharEl.classList.add("current");

    const char = cursorChars.typedChars.pop();
    cursorChars.remainingChars.unshift(char);
  }

  if (cursorChars.remainingChars.length === 0) {
    finishTest();
  }
};

const startTimer = () => {
  if (timerId === null) {
    if (currentMode === TIMER_MODE_VALUE) {
      const TIMED_MODE_MS = 60000;
      const startTime = Date.now();
      const endTime = Date.now() + TIMED_MODE_MS;

      timerId = setInterval(() => {
        const elapsedSec = Math.floor((Date.now() - startTime) / 1000);
        const remainingSec = Math.ceil((endTime - Date.now()) / 1000);
        const minutesForWPM = elapsedSec / 60;
        const seconds = remainingSec % 60;

        if (seconds <= 0) {
          finishTest();
        }

        wpm =
          minutesForWPM > 0
            ? Math.round(typedCharsCount / 5 / minutesForWPM)
            : 0;

        wpmEl.textContent = wpm;
        timeViewEl.textContent = `00:${formatTimer(seconds)}`;
      }, 1000);
    }

    if (currentMode === PASSAGE_MODE_VALUE) {
      const startTime = Date.now();

      timerId = setInterval(() => {
        const elapsedSec = Math.floor((Date.now() - startTime) / 1000);
        const minutes = Math.floor(elapsedSec / 60);
        const seconds = elapsedSec % 60;

        wpm =
          elapsedSec / 60 > 0
            ? Math.round(typedCharsCount / 5 / (elapsedSec / 60))
            : 0;

        wpmEl.textContent = wpm;
        timeViewEl.textContent = `${formatTimer(minutes)}:${formatTimer(seconds)}`;
      }, 1000);
    }

    if (!timeViewEl.classList.contains("started")) {
      timeViewEl.classList.add("started");
    }
  }
};

const startTypingTest = () => {
  if (state.loaded) {
    restartBtnContainer.classList.remove("hide");
    document.addEventListener("keydown", handleKeydown);
    startTimer();
    state.running = true;
  }
};

const startOnClick = () => {
  if (!startContainer.classList.contains("hide")) {
    blurMask.classList.remove("active");
    startContainer.classList.add("hide");
    startTypingTest();
    mobileInput.focus({ preventScroll: true });
  }
  if (document.activeElement !== mobileInput) {
    mobileInput.focus({ preventScroll: true });
  }
};

const setListeners = () => {
  difficultyOptions.forEach((node) => {
    node.addEventListener("click", setDifficulty);
  });
  modeOptions.forEach((node) => {
    node.addEventListener("click", setMode);
  });
  startBtn.addEventListener("mousedown", (evt) => {
    evt.preventDefault();
    startOnClick();
  });
  containerWords.addEventListener("click", (evt) => {
    if (evt.target === startBtn) {
      return;
    }
    startOnClick();
  });
  mobileTitleDropdown.forEach((node) => {
    node.addEventListener("touchstart", () => {
      node.nextElementSibling.classList.toggle("show");
    });
  });
  restartFinishBtn.addEventListener("mousedown", restartTestAfterFinish);
  restartBtn.addEventListener("mousedown", restartTest);
};

setListeners();
setupTest();
